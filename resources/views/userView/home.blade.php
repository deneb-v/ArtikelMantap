            <p class="text-break">Jakarta - Ratusan astronom memperingatkan bahwa konstelasi satelit seperti
                jaringan Starlink milikSpaceX bisa memiliki dampak yang besar bagi kemajuan astronomi dan sains.
            </p>

            <p class="text-break">Menurut laporan dari workshop Satellite Constellations 1 (Satcon1) konstelasi
                ribuan satelit yang terlihat sangat cerah bisa mengubah astronomi serta mempengaruhi langit malam
                untuk stargazer di seluruh dunia.
            </p>

            <p>
                Riset ini menggandeng lebih dari 250 astronom, operator satelit dan advokat langit malam untuk
                memahami lebih baik dampak dari konstelasi satelit raksasa kepada astronomi.
            </p>

            <p>
                "Kami menemukan bahwa kasus terburuk desain konstelasi terbukti sangat berdampak pada program sains
                yang paling terpengaruh," tulis laporan tersebut, seperti dikutip detikINET dari The Independent,
                Kamis (27/8/2020).
            </p>

            <p>
                Ribuan satelit yang terlihat sangat cerah dari Bumi ini telah lama menjadi perhatian ilmuwan dan
                operator seperti SpaceX. Tapi hingga saat ini belum ada solusi yang bisa mengatasinya.
            </p>

            <p>
                SpaceX sendiri berencana meluncurkan 30.000 satelit Starlink untuk menyediakan internet berkecepatan
                tinggi di Bumi. Selain SpaceX, ada juga beberapa operator lainnya yang memiliki tujuan serupa
                seperti Amazon dan Oneweb.
            </p>

            <p>
                Laporan ini menjabarkan enam pendekatan yang bisa diterapkan untuk mengatasi gangguan dari
                konstelasi satelit ini pada kegiatan astronomi.
            </p>

            <p>
                Pendekatan tersebut antara lain menetapkan batas ketinggian, menggelapkan permukaannya menggunakan
                'kacamata hitam' seperti VisorSat milik SpaceX, mengubah orientasi agar memantulkan lebih sedikit
                sinar matahari, menghapus wujudnya saat memproses gambar dan bekerja dengan operator untuk tidak
                mengarahkan teleskop ke arah satelit.
            </p>

            <p>
                Tapi hanya ada satu pendekatan dalam laporan tersebut yang bisa mengatasi masalah ini hingga ke
                akarnya, yaitu dengan tidak meluncurkan satelit.
            </p>

            <p>
                "Luncurkan lebih sedikit atau tidak satupun satelit LEO (orbit rendah Bumi). Betapapun tidak praktis
                atau tidak mungkin, ini adalah satu-satunya opsi yang teridentifikasi bisa mencapai nol dampak pada
                astronomi," jelas laporan tersebut.
            </p>

            <p>
                SpaceX memang telah beberapa kali dituduh merusak langit malam karena satelit Starlink yang
                jumlahnya makin banyak. Saat ini ada 500 satelit Starlink yang mengorbit, tapi SpaceX sudah berniat
                untuk menerbangkan 30.000 satelit dalam beberapa tahun ke depan.
            </p>

            <p>
                Saat awal diluncurkan pada tahun 2019, banyak orang yang mengira konstelasi ini sebagai UFO karena
                formasinya yang aneh, dan beberapa astronom menyebutnya sebagai sederet mutiara yang cerah.
                Baru-baru ini mereka juga dikritik karena merusak penampakan Komet Neowise saat melewati Bumi.
            </p>
